# Creating a tuple
t = (10, 20, 30, 40, 20)

print("Original tuple:", t)

# count()
print("Count of 20:", t.count(20))

# index()
print("Index of 30:", t.index(30))

# len()
print("Length of tuple:", len(t))

# max()
print("Maximum element:", max(t))

# min()
print("Minimum element:", min(t))

# sum()
print("Sum of elements:", sum(t))